n = input('Digite algo:')
print(n.isnumeric())
# isnumeric significa se a variavel em questão (n) é númerica ou não.

n2 = input('Digite algo:')
print(n2.isalpha())
# isalpha significa se a variavel em questão (n2) é uma letra ou não.

n3 = input('Digite algo:')
print(n3.isalnum())
# isalnum significa se a variavel em questão (n3) é uma letra ou número ou não.