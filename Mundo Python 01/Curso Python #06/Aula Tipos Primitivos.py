n = str(input('Digite um valor:'))
print(n)
# Reconhece qualquer tipo de caracter: Números, letras...

n = int(input('Digite um valor:'))
print(n)
# Reconhece apenas números inteiros. Ex. 1,2,3,-5...

n = float(input('Digite um valor:'))
print(n)
# Reconhece números inteiros ou quebrados. Ex. 7.0, 4,23...

n = bool(input('Digite um valor:'))
print(n)
# Reconhece apenas True ou False.